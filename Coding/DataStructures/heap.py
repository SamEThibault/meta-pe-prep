# Heaps
# - Specialized binary tree-based data structure used for priority-based operations
# - Max-Heap: value of each parent node is greater than or equal to its children
# - Min-Heap: value of each prent node is less than or equal to its children

# Completeness:
# - A heap is always a complete binary tree, meaning all levels are filled except possibly the last,
#   which is filled from left to right


